    public void informações(){
    String sql;

    try{
                //total de clientes
                sql = "select count(cod_cli) as 'total clientes' from cliente where situacao != 'DESCONHEC.'";
                con_serquip.executeSQL(sql);
                con_serquip.resultset.first();
                totalClientes.setText(con_serquip.resultset.getString("total clientes"));

                //total de clientes ativos
                sql = "select count(cod_cli) as 'total ativos' from cliente where situacao = 'ATIVO'";
                con_serquip.executeSQL(sql);
                con_serquip.resultset.first();
                qtClientesAtivos.setText(con_serquip.resultset.getString("total ativos"));

                //total de clientes suspensos
                sql = "select count(cod_cli) as 'total suspensos' from cliente where situacao = 'SUSPENSO'";
                con_serquip.executeSQL(sql);
                con_serquip.resultset.first();
                qtClientesSuspensos.setText(con_serquip.resultset.getString("total suspensos"));

                //total de clientes cancelados
                sql = "select count(cod_cli) as 'total cancelados' from cliente where situacao = 'CANCELADO'";
                con_serquip.executeSQL(sql);
                con_serquip.resultset.first();
                qtClientesCancelados.setText(con_serquip.resultset.getString("total cancelados"));

                //quantidade de bombonas de 200 litros
                sql = "select sum(qt_bomb200) as 'total 200' from cliente where situacao = 'ATIVO'";
                con_serquip.executeSQL(sql);
                con_serquip.resultset.first();
                qtBombContrato200.setText(con_serquip.resultset.getString("total 200"));

                //quantidade de bombonas de 50 litros
                sql = "select sum(qt_bomb50) as 'total 50' from cliente where situacao = 'ATIVO'";
                con_serquip.executeSQL(sql);
                con_serquip.resultset.first();
                qtBombContrato50.setText(con_serquip.resultset.getString("total 50"));

                //quantidade de bmbonas de 20 litros
                sql = "select sum(qt_bomb20) as 'total 20' from cliente where situacao = 'ATIVO'";
                con_serquip.executeSQL(sql);
                con_serquip.resultset.first();
                qtBombContrato20.setText(con_serquip.resultset.getString("total 20"));


                //quantidade total de bombonas usadas pelos clientes
                sql = "select sum(qt_bomb_total) as 'total bombonas' from cliente where situacao = 'ATIVO'";
                con_serquip.executeSQL(sql);
                con_serquip.resultset.first();
                qtBombContratoTotal.setText(con_serquip.resultset.getString("total bombonas"));


    }
    catch(Exception e){
        JOptionPane.showMessageDialog(null, e);



    }
    }

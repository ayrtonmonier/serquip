  public void desbloqueia(){

     //criptografa a senha para validação com a do banco
     criptografaSenha(senhaUsuario.getText().toString());

    if(senhaCriptografada.equals(senha)){

        telaPrincipal.lbAberto.setIcon(new javax.swing.ImageIcon(getClass().getResource("/imagens/aberto.png")));
        telaPrincipal.aberto = true;
        telaPrincipal.lb_contador.setForeground(new java.awt.Color(0, 0, 0));
        telaPrincipal.lb_contador.setText("5");
        telaPrincipal.bloqueiaTela.stop();
        con_parcialmenteBloqueado.desconecta();
        this.dispose();

    }
    else{
        cont--;
            if(cont >0){
                JOptionPane.showMessageDialog(null, "Senha incorreta, você ainda tem "+cont+" tentativa(s)");
                senhaUsuario.setText("");
                senhaUsuario.requestFocus();
            }

            else if(cont == 0){
                JOptionPane.showMessageDialog(null, "Para sua segurança sua matricula será bloqueada e o sistema será fechado");

                String sql = "UPDATE usuario SET situacao = 'Bloqueado' where matricula_usuario = '"+validaLogin.MATRICULA+"'";
                    try{
                        con_parcialmenteBloqueado.statement.executeUpdate(sql);
                        con_parcialmenteBloqueado.desconecta();

                        //avisa motivo logout
                        auxiliar.validaLogin.SITUACAO_LOGOUT = "Erro senha (desbloq.)";
                        telaAcesso.guardaLogout();

                        System.exit(0);
                    }
                    catch(Exception e){

                        JOptionPane.showMessageDialog(null, "Erro ao tentar bloquear usuário "+e);
                        con_parcialmenteBloqueado.desconecta();
                        System.exit(0);

                    }//fim catch

            }//fim else cont ==0


    }//fim else


}

  public void criptografaSenha(String senha){

    con_parcialmenteBloqueado.executeSQL("select password('"+senha+"') as 'senha_criptografada'");
    try{
        con_parcialmenteBloqueado.resultset.first();

        senhaCriptografada = con_parcialmenteBloqueado.resultset.getString("senha_criptografada");

    }
    catch(Exception e){
        System.out.println("A senha não pode ser criptografada.\nErro: "+e);

    }

}


    public static void atualizaInfoSuporte(){

            conexao con_sobre = new conexao();
            con_sobre.conecta();
            con_sobre.executeSQL("select * from info_suporte");
        try{
            
            con_sobre.resultset.first();
            lb_email.setText(con_sobre.resultset.getString("email_suporte"));
            lb_fone.setText(con_sobre.resultset.getString("tel_suporte"));

            con_sobre.desconecta();

        }
        catch(Exception e){
            System.out.print("Não foi possível atualizar o as informações de suporte");
        }

    }

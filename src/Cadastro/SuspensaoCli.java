    private void btSuspenderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSuspenderActionPerformed
        if(validaCadCli.modificado == true){
            validaCadCli.verificaCamposSuspensao();

            //ve se os campos estao preenchidos
            if(validaCadCli.campoVazio == false){
                    //ve se os campos estao preenchidos
                    validaCadCli.gravaSuspensaoCli();

                    //se gravou no banco só fecha
                    if(validaCadCli.gravadoNoBanco == true){
                        validaCadCli.gravadoNoBanco = false;//volta ao normal
                        dispose();
                    }
                    else
                        return;
            }
            else
              return;
        }
        else
            dispose();
    }//GEN-LAST:event_btSuspenderActionPerformed


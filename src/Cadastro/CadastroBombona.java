    private void btNovoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btNovoActionPerformed
        
        if(codigoBomb.getText().length() >= 1){


                validaCadBomb.atualizaPainel();
                validaCadBomb.habilitaCampos(true);
                validaCadBomb.limpaCamposCadBomb();
                btExcluir.setEnabled(false);
        }
        else if(cliente.getText().length() > 0){
            //pergunta se que limpar realmente
            int opcao = JOptionPane.showConfirmDialog(null , "ISSO APAGARÁ O REGISTRO ATUAL, DESEJA CONTINUAR?", "Novo registro", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);

            if(opcao == JOptionPane.YES_OPTION){

                validaCadBomb.atualizaPainel();
                validaCadBomb.habilitaCampos(true);
                validaCadBomb.limpaCamposCadBomb();

                btExcluir.setEnabled(false);
            }
            else{

                btExcluir.setEnabled(true);
                validaCadBomb.habilitaCampos(true);
                escolhaCapacidade.requestFocus();
                return;
            }
        }
        else{
                btExcluir.setEnabled(false);
                validaCadBomb.habilitaCampos(true);
                validaCadBomb.limpaCamposCadBomb();
        }

    }//GEN-LAST:event_btNovoActionPerformed


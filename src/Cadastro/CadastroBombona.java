    private void pesarAcumuladasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pesarAcumuladasActionPerformed
        Data.dataOntem();

        //JOptionPane.showMessageDialog(null, Data.dataOntem);

        if(pesarAcumuladas.isSelected() == true){
            validaCadBomb.dataPesagem = Data.dataOntem;
            validaCadBomb.tipoPesagem = "Acumulada";

            status.setForeground(new java.awt.Color(255, 0, 0));
            status.setText("Pesando bombona acumulada...");
            //JOptionPane.showMessageDialog(null, "Data de ontem: "+validaCadBomb.dataPesagem+"\nTipo de pesagem: "+validaCadBomb.tipoPesagem);

        }
        else{
            validaCadBomb.dataPesagem = validaLogin.DATAFORMAT;
            validaCadBomb.tipoPesagem = "Em dia";

            status.setForeground(new java.awt.Color(0, 0, 0));
            status.setText("Pesando bombona em dia...");
            //JOptionPane.showMessageDialog(null, "Data de hoje: "+validaCadBomb.dataPesagem+"\nTipo de pesagem: "+validaCadBomb.tipoPesagem);
        }
        //foco para o campo contrato
        contrato.requestFocus();

    }//GEN-LAST:event_pesarAcumuladasActionPerformed

